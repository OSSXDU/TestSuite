#include "packet.h"

using namespace std;

packet::packet() { }

packet::~packet() { }

///////////////////////////////////////////////////////////////////////////
//set id
///////////////////////////////////////////////////////////////////////////
void packet::set_id(string identification) { id=identification; }

///////////////////////////////////////////////////////////////////////////
//set status
///////////////////////////////////////////////////////////////////////////
void packet::set_status(int vehicle_status) { status=vehicle_status; }

///////////////////////////////////////////////////////////////////////////
//set data with id
///////////////////////////////////////////////////////////////////////////
void packet::set_data(string id, double rval) { data_map.find(id)->second.rval = rval; }

///////////////////////////////////////////////////////////////////////////
//set data
///////////////////////////////////////////////////////////////////////////
void packet::set_data(variable &var) { data_map[var.name] = var; }

///////////////////////////////////////////////////////////////////////////
//get id
///////////////////////////////////////////////////////////////////////////
string packet::get_id() { return id; }

///////////////////////////////////////////////////////////////////////////
//get status
///////////////////////////////////////////////////////////////////////////
int packet::get_status() { return status; }

///////////////////////////////////////////////////////////////////////////
//get ndata
///////////////////////////////////////////////////////////////////////////
int packet::get_data_size(){ return data_map.size(); }

///////////////////////////////////////////////////////////////////////////
//get data
///////////////////////////////////////////////////////////////////////////
std::map<string,variable>* packet::get_data() { return &data_map; }
